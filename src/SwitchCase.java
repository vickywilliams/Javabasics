public class SwitchCase {
    public static void main(String[] args) {
        int i = 4;
        switch (i) {
            case 1:
                System.out.println("num 1");
                break;
            case 2:
                System.out.println("num 2");
                break;
            case 3:
                System.out.println("num 3");
                break;
            case 4:
                System.out.println("num 4");
                break;
                           default:
                break;
        }
    }
}
